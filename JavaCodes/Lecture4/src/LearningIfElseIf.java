public class LearningIfElseIf {

    public static void main(String[] args) {

        boolean isSeniorCitizen = false;
        boolean isAdult = true;

        if (isSeniorCitizen) System.out.println("Hello Senior Citizen");

        else if (isAdult) System.out.println("Hello Adult");

        else System.out.println("Hello child");

    }
}
