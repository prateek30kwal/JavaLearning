public class Arithmetic {
    public static void main(String[] args) {

        int a = 8;
        int b = 3;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println((float)a/b);

        double x = 8;
        double y = 3;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

        System.out.println("Check modulus operator");
        System.out.println(a%b);
        System.out.println(x%y);


    }
}
